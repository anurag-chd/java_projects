package Basics;
public class Node <E> {
	
	private E value;
	private Node next;

	public Node(E num){
		this.value = num;
		this.next = null;
	}
	
	public void setvalue(E num){
		this.value = num;
	}
	
	public void setNext(Node Next){
		this.next = Next;
	}
	public E getvalue(){
		return (E)this.value;
	}
	public Node getNext(){
		return this.next;
	}
	

}
